export function getMagicEffectId(effect: string | number | undefined): number {
  if (typeof effect === "number") {
    return Math.max(1, Math.min(65_535, Math.floor(effect)));
  }
  return effect ? (EFFECT_IDS[effect] ?? 3) : 3;
}
